# Rails Tasks

# são tarefas predefinidas que o rails pode executar

# para conhecer todas as tasks disponiveis rode

# rails -T

# ou

# rails -T db

# as tasks de bd mais usadas são:

# rails db:migrate      executa as migrations pendentes
# rails db:rollback     desfaz a ultima migration
# rails db:drop         deleta o banco de dados
# rails db:create       cria o banco de dados

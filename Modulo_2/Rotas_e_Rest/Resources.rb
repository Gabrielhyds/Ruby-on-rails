# Resources

# Quando usamos a palavra resources no mapeamento
# das rotas do Rails, de forma automática ele entenderá
# que queremos criar 7 rotas padrão para o CRUD.

# Crie suas próprias Rotas

# Para criar suas próprias rotas apenas declare o verbo e
# a url e o controller e action que deve responder à url
# indicada, veja o exemplo:

# -> get '/inicio', to: 'welcome#index'

# A rota acima permitirá que seja digitada a url /inicio e
# ela acessará o controller welcome e ação index.

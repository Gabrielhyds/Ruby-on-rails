# Layouts

# ● Quando acessamos uma URL o Rails, no momento em
# que vai enviar a resposta para o usuário, renderiza um
# layout padrão (application) antes de renderizar a view
# que solicitamos.

# ● Se quisermos alterar o layout padrão, basta no
# controller indicarmos o layout que deseja-se
# renderizar.
# ○ layout "nome_do_layout"

# ● Documentação
# ○ https://api.rubyonrails.org/classes/ActionView/Layouts.html
# ○ https://guides.rubyonrails.org/layouts_and_rendering.html
